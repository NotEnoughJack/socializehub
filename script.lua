local LoadedAnims = {}

for category, animList in pairs(AnimationCategories) do
    local tab = Window:AddTab({ Title = category, Icon = "play-circle" })
    Tabs[category] = tab

    for _, anim in ipairs(animList) do
        local toggle = tab:AddToggle(anim.Name, {
            Title = anim.Name,
            Default = false,
            Description = "Toggle to play/stop animation"
        })

        toggle:OnChanged(function(value)
            if value then
                local animation = Instance.new("Animation")
                animation.AnimationId = anim.Id
                local track = humanoid:LoadAnimation(animation)
                track:Play()
                LoadedAnims[anim.Name] = track
            else
                local track = LoadedAnims[anim.Name]
                if track then
                    track:Stop()
                    LoadedAnims[anim.Name] = nil
                end
            end
        end)
    end
end

